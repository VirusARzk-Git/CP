int gcd(int a, int b)
{
    if (b == 0)
        return a;
    return gcd(b, a % b);
}

int pow(int x, int n)
{
    if (!n)
        return 1;
    int temp = pow(x, n / 2);
    temp *= temp;
    if (n % 2)
        return temp * x;
    else
        return temp;
}

int pow(int x, int n, int md)
{
    if (!n)
        return 1;
    int temp = pow(x, n / 2, md);
    temp = (temp * temp) % md;
    if (n % 2)
        return (temp * x) % md;
    else
        return temp;
}

