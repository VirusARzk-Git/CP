bool check(int n, int m)
{
    int ab = __gcd(n, m);
    int a = n / ab;
    int b = m / ab;
    int c1 = 0;
    while (a > 1)
    {
        if (a % 3)
            return false;
        c1++;
        a /= 3;
    }
    int c2 = c1;
    c1 = 0;
    while (b > 1)
    {
        if (b % 2)
            return false;
        c1++;
        b /= 2;
    }
    if (c1 <= c2)
        return true;
    else
        return false;
}

