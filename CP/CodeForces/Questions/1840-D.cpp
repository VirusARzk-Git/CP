bool can_achieve_time(vector<int> &arr, int n, int mid)
{
    int curr = arr[0] + mid, cnt = 1;
    for (int i = 1; i < n; i++)
        if (abs(curr - arr[i]) > mid)
            cnt++, curr = arr[i] + mid;

    if (cnt <= 3)
        return true;
    return false;
}

